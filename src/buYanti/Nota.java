    private void tampilkanDataNota() {
        namaCust.setText(this.namaPelanggan);
        tglCust.setText(this.tanggalTransaksi);
        alaCust.setText(this.alamatPelanggan);
        
        DefaultTableModel model = (DefaultTableModel) tblLaundry.getModel();
        model.setRowCount(0); // Kosongkan tabel
        
        for (Object[] item : daftarLayanan) {
            model.addRow(item);
        }
    }
    
